    private void table_update()
        {
            int c;
            
            try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/student management","root","");
            insert = con1.prepareStatement("select * from marks_details");
            ResultSet rs = insert.executeQuery();
            ResultSetMetaData Rss = rs.getMetaData();
            c = Rss.getColumnCount();
            
            DefaultTableModel Df = (DefaultTableModel)jTable1.getModel();
            Df.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for(int a = 1; a <= c; a++)
                {
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("rollno"));
                    v2.add(rs.getString("studentname"));
                    v2.add(rs.getString("aoa1"));
                    v2.add(rs.getString("mp1"));
                    v2.add(rs.getString("os1"));
                    v2.add(rs.getString("dbms1"));
                    v2.add(rs.getString("maths1"));
                    v2.add(rs.getString("aoa2"));
                    v2.add(rs.getString("mp2"));
                    v2.add(rs.getString("os2"));
                    v2.add(rs.getString("dbms2"));
                    v2.add(rs.getString("maths2"));
                }
                
                Df.addRow(v2);
            }
            
            txtrollno.setText("");
            txtname.setText("");
            m1aoa.setText("");
            m1mp.setText("");
            m1os.setText("");
            m1dbms.setText("");
            m1maths.setText("");
            m2aoa.setText("");
            m2mp.setText("");
            m2os.setText("");
            m2dbms.setText("");
            m2maths.setText("");
            txtrollno.requestFocus();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(sms.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(sms.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    
