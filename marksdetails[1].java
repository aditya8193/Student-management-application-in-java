    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        JComboBox<String> combo = (JComboBox<String>) evt.getSource();
        String selectedFaculty = (String) combo.getSelectedItem();
        if(selectedFaculty.equals("Passed Students")){
            passedstudents_filter();
        }
        else if(selectedFaculty.equals("Failed Students")){
            failedstudents_filter();
        }
        else if(selectedFaculty.equals("All")){
            table_update();
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

